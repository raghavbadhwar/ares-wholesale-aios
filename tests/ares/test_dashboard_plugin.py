def test_ares_dashboard_get_report(
    tmp_path: Path,
    monkeypatch,
) -> None:
    monkeypatch.setenv("ARES_HOME", str(tmp_path / ".ares"))
    write_client_profile(ClientProfile(client_slug="demo", business_name="Demo Wholesale", owner_name="Owner"))

    # Create dummy reports folder and a dummy markdown report
    reports_dir = tmp_path / ".ares" / "clients" / "demo" / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)
    dummy_report = reports_dir / "daily_audit.md"
    dummy_report.write_text("# Daily Audit Report\n\nAll systems nominal.", encoding="utf-8")

    # Happy path
    response = _client().get(f"/report?client=demo&path={dummy_report.absolute()}")
    assert response.status_code == 200
    payload = response.json()
    assert payload["name"] == "daily_audit.md"
    assert "Daily Audit Report" in payload["content"]

    # Traversal attempt outside client reports directory
    outside_file = tmp_path / "outside.md"
    outside_file.write_text("secrets", encoding="utf-8")
    response_traversal = _client().get(f"/report?client=demo&path={outside_file.absolute()}")
    assert response_traversal.status_code == 400
    assert "Invalid report path" in response_traversal.json()["detail"]


