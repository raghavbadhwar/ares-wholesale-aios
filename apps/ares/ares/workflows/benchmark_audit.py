def build_benchmark_completion_audit(
    *,
    latest_local_test_result: str,
    integration_preflight: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Return a current-state audit that separates local coverage from production proof."""
    production_blockers = list(PRODUCTION_BLOCKERS)
    if integration_preflight and integration_preflight.get("status") != "ready":
        production_blockers.append("external_integration_preflight_blocked")
    feature_rows = [_copy_feature_row(row) for row in FEATURE_EVIDENCE_ROWS]
    missing_evidence = [
        row["name"]
        for row in feature_rows
        if not row["implementation_slice"] or not row["workflow_files"] or not row["test_files"]
    ]
    rows_with_production_proof = sum(1 for row in feature_rows if row["production_proof"]["status"] == "proven")
    return {
        "mode": "local_audit",
        "feature_rows_total": len(FEATURE_ROWS),
        "local_or_contract_rows_covered": len(FEATURE_ROWS),
        "latest_local_test_result": latest_local_test_result,
        "benchmark_parity": False,
        "ship_ready": False,
        "feature_evidence": {
            "source_of_truth_feature_rows_traced": not missing_evidence and len(feature_rows) == len(FEATURE_ROWS),
            "feature_rows_with_evidence": len(feature_rows) - len(missing_evidence),
            "feature_rows_missing_evidence": missing_evidence,
            "feature_rows_with_production_proof": rows_with_production_proof,
        },
        "feature_rows": feature_rows,
        "production_blockers": production_blockers,
        "integration_preflight": _summarize_integration_preflight(integration_preflight),
        "done_state_gates": [{"gate": gate, "status": "not_proven"} for gate in DONE_STATE_GATES],
        "audit": {
            "local_tests_are_sufficient_for_ship_ready_claim": False,
            "external_integrations_verified": False,
            "hosted_saas_verified": False,
            "longitudinal_business_outcomes_verified": False,
            "limitation": BENCHMARK_AUDIT_LIMITATION,
        },
    }


def _copy_feature_row(row: dict[str, Any]) -> dict[str, Any]:
    production_proof = row["production_proof"]
    return {
        "name": row["name"],
        "module": row["module"],
        "priority": row["priority"],
        "status": row["status"],
        "coverage_type": row["coverage_type"],
        "implementation_slice": row["implementation_slice"],
        "workflow_files": list(row["workflow_files"]),
        "test_files": list(row["test_files"]),
        "current_limitation": row["current_limitation"],
        "production_proof": {
            "status": production_proof["status"],
            "required_evidence": list(production_proof["required_evidence"]),
        },
    }


def _summarize_integration_preflight(integration_preflight: dict[str, Any] | None) -> dict[str, Any]:
    if not integration_preflight:
        return {"included": False}
    blocked_providers = []
    for provider, status in integration_preflight.get("providers", {}).items():
        if status.get("status") == "ready_for_sandbox_adapter_tests":
            continue
        blocked_providers.append(
            {
                "provider": provider,
                "status": status.get("status"),
                "missing_env_names": list(status.get("missing_env_names", [])),
                "safe_test_environment_confirmed": status.get("safe_test_environment_confirmed", False),
                "can_run_sandbox_adapter_tests": status.get("can_run_sandbox_adapter_tests", False),
                "blocked_reasons": list(status.get("blocked_reasons", [])),
                "benchmark_feature_rows": list(status.get("benchmark_feature_rows", [])),
                "production_blockers_addressed": list(status.get("production_blockers_addressed", [])),
            }
        )
    audit = integration_preflight.get("audit", {})
    return {
        "included": True,
        "mode": integration_preflight.get("mode"),
        "status": integration_preflight.get("status"),
        "provider_scope": list(integration_preflight.get("provider_scope", [])),
        "ready_provider_count": integration_preflight.get("ready_provider_count", 0),
        "blocked_provider_count": integration_preflight.get("blocked_provider_count", 0),
        "blocked_providers": blocked_providers,
        "audit": {
            "secret_values_inspected": audit.get("secret_values_inspected", False),
            "live_api_called": audit.get("live_api_called", False),
            "sandbox_submission_performed": audit.get("sandbox_submission_performed", False),
        },
    }
